function dataFormatter(obj) {
    var pList = ['高桥西','高桥','梁祝','芦港','徐家漕长乐','望春桥','泽民','大卿桥','西门口','鼓楼','东门口','江厦桥东','舟孟北路','樱花公园','福明路','世纪大道','海晏北路','福庆北路','盛莫路','东环南路'];
    var temp;
    var max = 0;
    for (var year = 2002; year <= 2008; year++) {
        temp = obj[year];
        for (var i = 0, l = temp.length; i < l; i++) {
            max = Math.max(max, temp[i]);
            obj[year][i] = {
                name : pList[i],
                value : temp[i]
            }
        }
        obj[year+'max'] = Math.floor(max/100) * 100;
    }
    return obj;
}

function dataMix(list) {
    var mixData = {};
    for (var i = 0, l = list.length; i < l; i++) {
        for (var key in list[i]) {
            if (list[i][key] instanceof Array) {
                mixData[key] = mixData[key] || [];
                for (var j = 0, k = list[i][key].length; j < k; j++) {
                    mixData[key][j] = mixData[key][j] 
                                      || {name : list[i][key][j].name, value : []};
                    mixData[key][j].value.push(list[i][key][j].value);
                }
            }
        }
    }
    return mixData;
}

